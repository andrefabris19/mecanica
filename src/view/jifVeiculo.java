    private void jtbVeiculoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbVeiculoMouseClicked
        int linhaSelecionada = jtbVeiculo.getSelectedRow();
        if (linhaSelecionada > -1) {
            jtfChassi.setText("" + listaveiculos.get(linhaSelecionada).getChassi());
            jtfModelo.setText("" + listaveiculos.get(linhaSelecionada).getModelo());
            jtfPlaca.setText("" + listaveiculos.get(linhaSelecionada).getPlaca());
            jtfAno.setText("" + listaveiculos.get(linhaSelecionada).getAno_veiculo_veiculo());
            jcbClientes.setSelectedItem(listaveiculos.get(linhaSelecionada).getNome());
            jbEditar.setEnabled(true);
            jbExcluir.setEnabled(true);
        } else {
            jbEditar.setEnabled(false);
            jbExcluir.setEnabled(false);
        }
    }//GEN-LAST:event_jtbVeiculoMouseClicked

    private void jtfBuscaVeiculoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfBuscaVeiculoKeyReleased
        try {
            listaveiculos = DAOVeiculo.pesquisar(jtfBuscaVeiculo.getText());
            DefaultTableModel mp = (DefaultTableModel) jtbVeiculo.getModel();
            mp.setNumRows(0);
            for (VeiculoModel u : listaveiculos) {
                mp.addRow(new String[]{"" + u.getChassi(), u.getModelo(), u.getPlaca()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao pesquisar:" + e.getMessage());
        }
    }//GEN-LAST:event_jtfBuscaVeiculoKeyReleased


