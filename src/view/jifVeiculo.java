private boolean validarCampos() {
        String msg = "";
        if (jtfChassi.getText().length() < 3 || jtfChassi.getText().length() > 100) {
            msg += "\nO campo Chassi deve conter 17 caracteres";
        }

        if (jtfModelo.getText().length() < 3 || jtfModelo.getText().length() > 100) {
            msg += "\nO campo Modelo deve conter entre 3 e 100 caracteres";
        }

        if ((jtfPlaca.getText().length()) != 7) {
            msg += "\nO campo Placa deve conter 7 caracteres";
        }

        if ((jtfAno.getText().length()) != 4) {
            msg += "\nO campo Ano deve conter 4 caracteres";
        }

        if (msg.isEmpty()) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Os campos abaixo precisam de atenção:"
                    + msg, "Atenção", JOptionPane.ERROR_MESSAGE);
        }
        return false;
    }

    private boolean preencherObjeto() throws Exception {
        obj.setChassi(jtfChassi.getText());
        obj.setModelo(jtfModelo.getText());
        obj.setPlaca(jtfPlaca.getText());
        obj.setAno_veiculo_veiculo(Integer.parseInt(jtfAno.getText()));
        obj.setFk_cli_id(jcbClientes.getSelectedIndex());
        //obj.setFk_cli_id(listaveiculos.get(jcbClientes.getSelectedIndex()-1));
        return true;
    }

