    private boolean validarCampos() {
        String msg = "";
        if (jtfNomeServico.getText().length() < 3 || jtfNomeServico.getText().length() > 100) {
            msg += "\nO campo Nome deve conter entre 3 e 100 caracteres";
        }

        if (jtfDescricaoServico.getText().length() < 3 || jtfDescricaoServico.getText().length() > 200) {
            msg += "\nO campo Descrição deve conter entre 3 e 200 caracteres";
        }

        if (jtfValor.getText().length() < 1 || jtfValor.getText().length() > 20) {
            msg += "\nO campo Valor deve conter entre 1 e 20 caracteres";
        }

        if (msg.isEmpty()) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Os campos abaixo precisam de atenção:"
                    + msg, "Atenção", JOptionPane.ERROR_MESSAGE);
        }
        return false;
    }

    private boolean preencherObjeto() throws Exception {
        obj.setIdServico(Integer.parseInt(jlIdServico.getText()));
        obj.setNomeServico(jtfNomeServico.getText());
        obj.setDescricao(jtfDescricaoServico.getText());
        obj.setValor(Double.parseDouble(jtfValor.getText()));
        return true;
    }

