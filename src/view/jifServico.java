    private void jtfbuscaServicoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfbuscaServicoKeyReleased
        try {
            listaservicos = DAO.pesquisar(jtfbuscaServico.getText());
            DefaultTableModel mp = (DefaultTableModel) jtbServicos.getModel();
            mp.setNumRows(0);
            for (ServicoModel u : listaservicos) {
                mp.addRow(new String[]{"" + u.getNomeServico(), u.getDescricao()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao pesquisar:" + e.getMessage());
        }
    }//GEN-LAST:event_jtfbuscaServicoKeyReleased

    private void jtbServicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbServicosMouseClicked
        int linhaSelecionada = jtbServicos.getSelectedRow();
        if (linhaSelecionada > -1) {
            jlIdServico.setText("" + listaservicos.get(linhaSelecionada).getIdServico());
            jtfNomeServico.setText("" + listaservicos.get(linhaSelecionada).getNomeServico());
            jtfDescricaoServico.setText("" + listaservicos.get(linhaSelecionada).getDescricao());
            jtfValor.setText("" + listaservicos.get(linhaSelecionada).getValor());
            jbEditar.setEnabled(true);
            jbExcluir.setEnabled(true);
        } else {
            jbEditar.setEnabled(false);
            jbExcluir.setEnabled(false);
        }
    }//GEN-LAST:event_jtbServicosMouseClicked

