    private void jtfBuscaMecanicoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfBuscaMecanicoKeyReleased
        try {
            listamecanicos = DAO.pesquisar(jtfBuscaMecanico.getText());
            DefaultTableModel mp = (DefaultTableModel) jtbMecanico.getModel();
            mp.setNumRows(0);
            for (MecanicoModel u : listamecanicos) {
                mp.addRow(new String[]{"" + u.getNome(), u.getEspecialidade(), u.getUsuario()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao pesquisar:" + e.getMessage());
        }
    }//GEN-LAST:event_jtfBuscaMecanicoKeyReleased

    private void jtbMecanicoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbMecanicoMouseClicked
        int linhaSelecionada = jtbMecanico.getSelectedRow();
        if (linhaSelecionada > -1) {
            jlIdMecanico.setText("" + listamecanicos.get(linhaSelecionada).getIdMec());
            jtfNome.setText("" + listamecanicos.get(linhaSelecionada).getNome());
            jtfEspecialidade.setText("" + listamecanicos.get(linhaSelecionada).getEspecialidade());
            jtfUsuario.setText("" + listamecanicos.get(linhaSelecionada).getUsuario());
            jpfSenha.setText("" + listamecanicos.get(linhaSelecionada).getSenha());
            jbEditar.setEnabled(true);
            jbExcluir.setEnabled(true);
        } else {
            jbEditar.setEnabled(false);
            jbExcluir.setEnabled(false);
        }
    }//GEN-LAST:event_jtbMecanicoMouseClicked


