    private boolean validarCampos() {
        String msg = "";
        if (jtfNome.getText().length() < 3 || jtfNome.getText().length() > 100) {
            msg += "\nO campo Nome deve conter entre 3 e 100 caracteres";
        }

        if (jtfEspecialidade.getText().length() < 3 || jtfEspecialidade.getText().length() > 100) {
            msg += "\nO campo Especialidade deve conter entre 3 e 100 caracteres";
        }
        
        if (jtfUsuario.getText().length() < 3 || jtfUsuario.getText().length() > 100) {
            msg += "\nO campo Usuário deve conter entre 3 e 100 caracteres";
        }
        
        if (jpfSenha.getText().length() < 5 || jpfSenha.getText().length() > 100) {
            msg += "\nO campo Senha deve conter entre 3 e 100 caracteres";
        }

        if (msg.isEmpty()) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Os campos abaixo precisam de atenção:"
                    + msg, "Atenção", JOptionPane.ERROR_MESSAGE);
        }
        return false;
    }

    private boolean preencherObjeto() throws Exception {
        obj.setIdMec(Integer.parseInt(jlIdMecanico.getText()));
        obj.setNome(jtfNome.getText());
        obj.setEspecialidade(jtfEspecialidade.getText());
        obj.setUsuario(jtfUsuario.getText());
        obj.setSenha(jpfSenha.getText());
        return true;
    }

