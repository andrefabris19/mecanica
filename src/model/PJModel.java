package model;

/**
 *
 * @author andre
 */
public class PJModel {
    String CNPJ;
    String IE;

    public PJModel(String CNPJ, String IE) {
        CNPJ = new String();
        IE = new String();
    }

    public String getCNPJ() {
        return CNPJ;
    }

    public void setCNPJ(String CNPJ) {
        this.CNPJ = CNPJ;
    }

    public String getIE() {
        return IE;
    }

    public void setIE(String IE) {
        this.IE = IE;
    }
}
